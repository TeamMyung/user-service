package com.sparta.userservice.global.security.jwt.user;

import com.sparta.userservice.domain.DeliveryManager;
import com.sparta.userservice.domain.User;
import lombok.Getter;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Collection;
import java.util.List;

@Getter
public class UserDetailsImpl implements UserDetails {

    private final User user;
    private final DeliveryManager deliveryManager;

    public UserDetailsImpl(User user) {
        this.user = user;
        this.deliveryManager = null;
    }

    public UserDetailsImpl(User user, DeliveryManager deliveryManager) {
        this.user = user;
        this.deliveryManager = deliveryManager;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return List.of(new SimpleGrantedAuthority("ROLE_" + user.getRole().name()));
    }

    @Override
    public String getPassword() {
        return user.getPassword();
    }

    @Override
    public String getUsername() {
        return user.getUsername();
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
}
